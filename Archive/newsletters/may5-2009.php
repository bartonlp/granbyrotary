<h3>Charter Granby Rotarian Gary Cooper Passed Away
Last Week</h3>

<p> Services set for Saturday, May 9 The funeral service for longtime
Granby Rotarian Gary Cooper is set for 1 p.m. Saturday, May 9 at the
SilverCreek Convention Center.  There will be a viewing at the Mormon
Church in the morning, the family will be present; graveside services
will be at 4 p.m.; and a potluck with a sharing time, will be at 5
p.m. at SilverCreek.  Gary fought courageously throughout his illness
and managed to attend several Rotary meetings and functions over the
last few years despite his deteriorating health.  Gary was the manager
of the SilverCreek Water and Sanitation District and did a great job
of bringing that one-time struggling district back to a solid footing.
He took over the district in the midst of the last financial downturn
back in the '80s.  Gary will be missed by the Club and his many
friends.
</p>

<hr/>

<h3>Former Granby Rotarian Joyce Ackerson Passed Away
Last Week In Texas</h3>

<p> One-time Granby Rotarian Joyce Ackerson passed away in Texas last
week. She resigned from our Club last year due to declining health and
business problems and then moved to Texas to be with her son. She was
an owner and operator of Grand County Abstract in Hot Sulphur Springs.
She also operated a wedding chapel and wedding service that was a
great joy to her.  As we get more information about services, etc., we
will inform our club members.
</p>

