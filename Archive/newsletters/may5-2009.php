<h3>Put May 12 Earth Day on Your Calendar</h3>

<p> Wednesday, May 6 noon Rotary Club program is a good preparation
for activities of our own special Earth Day Remember to mark your
calendars! Tuesday, May 12 is the day of our own special Earth Day
community event.  The program for tomorrow's (Wednesday, May 6)
meeting is a preparation for the work that takes place that day.  Rob
Milstead was invited by event organizer Rhonda Farrell to talk to the
membership about the ways to plant trees, care for them and to provide
basic information on landscaping in general. Rob is with Neils
Lunceford nursery and landscaping, which is also sponsoring trees and
activities during the May 12 Earth Day event.  What follows is a press
release about the event:
</p>

<div id="earthday">

<h4>Rotarians, 6th graders team up for Earth Day effort</h4>

<p>May 12 event entails cleaning up Granby and its parks and planting
trees</p>

<p>GRANBY -- The Rotary Club of Granby and East Grand Middle School
plan to conduct a combined Earth Day project the afternoon of Tuesday,
May 12 in Granby.</p>

<p class="quote"> The project entails picking up trash
around Granby and its town parks and planting trees donated by Neils
Lunceford, Inc. (landscapers and nursery) at the East Grand Middle
School. The work will be completed by 6th graders at the middle
school, some staff members and members of the Rotary Club of Granby.
</p>

<p class="quote">&quot;This Earth Day project provides an opportunity for East
Grand Middle School sixth graders to learn about the value of
protecting our planet by keeping it clean and healthy,&quot; said Rhonda
Farrell, the Granby Rotarian who&apos;s heading up this effort. &quot;For the
Rotary Club, this is one of many of our community service
projects.&quot;</p>

<p class="quote"> Farrell said the actual date for Earth Day was April
22. But in the high country, where snow and cold temperatures persist
through April, it made sense conduct the local celebration in May.
</p>

<p class="quote"> The Rotary Club will provide adult supervision,
T-shirts, gloves, trees and material for planting, along with the
labor for digging holes. Snacks will be provided and garbage bags will
be on hand. The Club will arrange the trash pickup.</p>

<p class="quote"> East Grand Middle School will provide team teachers
for supervision, students and an educational perspective since the
work qualifies as an &quot;ends&quot; service for the International
Baccalaureate program that&apos;s being instituted in the school
district.</p>

<p class="quote">&quot;The sixth grade students will get some education out
of this as well,&quot; Farrell said. &quot;Students will meet at the school
ahead of time and learn about Earth Day, their responsibility to the
Earth, the value of trees and the role of Rotary in the
community.&quot;</p>


<p class="quote">The Rotary Club of Granby participates and operates a
variety of public service projects in Granby including the Centennial
Bench program, the 9Health Fair, graduation scholarships and lunch at
the 4th of July festivities. The Rotary Club of Granby meets weekly at
noon every Wednesday in the Inn at SilverCreek.</p>

<p class="quote">It&apos;s expected that about 85 students and six
teachers will participate in this event. Rotarians will be on hand to
provide adult supervision and to complete some of the tasks with the
students.</p>

<p class="quote">For more information, contact Rhonda Farrell at
887-2734 or e-mail her at rfarrell@rkymtnhi.com.</p>
</div>

