<h3>Ecuador Update is on Tap for Program of Wednesday, March 4</h3>

<p>The program for this Wednesday's luncheon meeting at the Inn at
SilverCreek should be interesting. Eden Recor and Pam Gilbert will be
on hand to inform our club about the latest developments with the
Ecuadorian highland village improvements.</p>

<p>Eden Recor has been to the village to help on a water project and his
information, along with Pam's, should be interesting.</p>

<p>The Granby Rotary Club has heard about these efforts before and has
donated funds to the efforts to bring water, a school house and other
improvements to this isolated yet beautiful village high in the
Andes.</p>

