<h2>President Jan Knisley's Message</h2>

<p>As we start this new year in Rotary our club faces a budget short fall of
   about $1,000. Over the past couple of years our club's funds have been
   running lower. We need to develop new exciting fund raisers. One
   area we have neglected in the past is our winter season. During
   winter Grand County attracts a lot of winter sports enthusiasts and
   we need to have events that focus on these folks. Over the next
   couple of months the board with the help of all members will try to
   create new fund raising events that will tap into the winter sports
   fans. Any thoughts that members my have should be brought to the
   attention of the board. Contact board members at the weekly
   meetings or use the <b>Members</b> page on the web site to mail you
   ideas. The last thing we want to do is curtail our charitable
   activities due to a lack of funds.</p>
