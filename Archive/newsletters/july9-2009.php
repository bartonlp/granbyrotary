<h2>Fourth of July Picnic Fund Raiser Great Success</h2>

<p>The Granby Rotary Club's annual fourth of July picnic fund raiser
was a big success with over 600 meals sold. The club made $2,420 net
on the event. The annual Granby parade and picnic is our biggest fund
raiser and this year it was a great success thank to chairs Thomas
Chaffin and Bill Edelstein who did a tremendous job organizing the
event. Thanks also goes to the many volunteers that made the event
possible. This year hamburgers, or hot-dogs plus chips and a drink
were sold for $6 or $20 for a family of four. The weather cooperated
and we all had a beautiful day of parade, food and fun.</p>

