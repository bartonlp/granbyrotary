<h2>District Governor Mike Oldham Addresses Granby Rotary Club</h2>

<p>The new 2009/2010 District 5054 Governor Mike Oldham addressed our
   club at our regular July 8 noon business meeting. Mike emphasized
   Rotary's commitment to the &quot;End Polio Now&quot; program. The
   goal for this year is to raise $100,000,000 by October 24
   &quot;Polio Day&quot;. This years thrust is to get the community
   involved in giving. The district is working on a new web site to
   collect donations from the public. The web page is
   http://www.endpolio.org, it will be updated during the next month
   for the new year's campaign.</p>
<p>Mike met with the Granby Rotary Club board of directors prior to
   the meeting and discussed our plans for the new year and fund
   raising (see presidents message below). Mike pledged the districts
   and his own support to help us grow our membership and develop
   better fund raising events.</p>

