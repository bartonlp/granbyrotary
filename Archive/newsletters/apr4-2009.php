<p>There's been discussion at the Dollywood level of sending paperback
books instead of hardbacks. Rotarian Jack Bakken replied, urging the
continued use of hard backs.</p>

<pre>
Hi Patrick
 
The Dolly Parton Imagination Library folk asked us to comment on their
proposal to go to soft cover books instead of hard-backs.  My response
was nowhere as eloquent as Tegan's.  Please print this in your next
newsletter so that all of our club members can read it.  Barton may
find a way to put it onto our website, too.  If any of our members
want to send their comments to David Dotson at the Dollywood
Foundation that would also help to keep the program at the high
quality that it is.   

Jack

</pre>

<p>What follows is the response of Tegan
Davis, the Granby Library's Youth Services Librarian. Jack thought
this was worth sending out.</p>

<pre>
From: "Tegan Davis" <tdavis@gcld.org>
To: &lt;ddotson@dollyfoundation.com&gt;
Cc: "Jack Bakken" 
Date: Thu, 2 Apr 2009 18:04:27 -0600
Subject: In regards to Soft Covered Books

Good Afternoon David,
 
The Granby Public Library in conjunction with Grand Beginnings and the
Granby Rotary Club kicked off the Imagination Library program in the
middle of November.  To date we have over seventy children signed up
for this program.  The positive response has been amazing!   

With assistance from the Grand County Library District, the Granby
Rotary Club was able to encourage the other Rotary Clubs in our
county, who were not participating in the program, to begin the
necessary steps to start up the Imagination Library.  The goal is to
have the Imagination Library program county wide, reaching all of our
children!   

I strongly believe that the success of this program is due to the
quality of books provided to the children.  Many of the parents looked
at the display books, commented with enthusiasm that they were durable
hardcovers, age appropriate, and signed their children up.  The
introduction of soft cover books into the Imagination Library would
definitely compromise the quality of the program.   

As the Youth Services Librarian, I've observe our little patrons as
they browse our collection and they tend to gravitate towards
hardcover books.  The content might be the same inside a soft cover
and hardcover book, but to a child the hardcover is more desirable.
They understand the quality difference and they intuitively choose to
check out hardcovers books.   

I feel that the Imagination Library is a wonderful resource for our
children.  It's designed to encourage reading and the love for books.
Many of the children who are participating in this program have
brought their books into the library to show me what they got in the
mail.  They are so excited!  I feel this excitement will wane if soft
cover books are introduced.   

If the book the child received in the mail from the Imagination
Library becomes their favorite book, will a soft cover last the number
of times the book is read?  Will it last to be passed on to siblings,
cousins, or their own children?  Hardcover books have proven to last
over the years and it's the quality the parents signed up for.   

I understand that during these difficult financial times it has become
harder to fund raise for programs like the Imagination Library.
However, we have great community support for this program and the
positive impact it will make upon our children that I feel we will be
able to sustain the growth of this program without the introduction of
soft cover books.   

Thanks for all your hard work,
 
Tegan Davis
Youth Services Librarian
Granby Library
</pre>

