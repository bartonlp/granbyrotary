<h2>Club Matches Donations</h2>

<p>The Granby Rotary Club Board of Directors decided to offer up to
$500 in club funds to meet the needs of the food bank and Granby
preschool. The board decided June 1 to match up to $250 for each of
two entities &mdash; the <b>Mountain Food Bank</b> and the new
<b>Granby Playdays Preschool</b>. As many know, the demands being put
on food banks has picked up recently. Our club has even conduced
canned food drives to help out. But now the board has decided to take
a different approach and offer $500 in cash to the food bank if club
members come up with $250. Our club will match each gift up to $250.
The board is taking the same approach with responding to a request for
funds to help with the start-up of a new preschool in Granby, called
<b>Granby Playdays Preschool</b>. The town of Granby has donated a
building for the preschool, but remodeling costs (for materials), are
needed.  The new preschool is being formed because the <b>C Lazy U
Preschool</b> closed this spring and the Mountain Family Center's
preschool in Grand Lake also closed.</p>

