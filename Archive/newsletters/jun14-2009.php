<h2>President's Message Emphasized Positive</h2>

<p>During the last business meeting he will preside over as president
of the Club, Tim Schowalter had good news about our successes tempered
with a reality check.  The reality check was this: Our club is
granting out more money than it brings in through fundraisers. That's
good as it means we are making a mark in our community. It's bad
because it's hurting our fund balance. He urged a continued effort to
find more ways to raise funds or we might have to cut programs. He
said that will be a priority decision for our new, incoming president,
Jan Knisley.  Schowalter also highlighted how we still need to get new
members into the Granby Club. He said the club has many new members,
but that has been offset by the losses of longstanding members for a
variety of reasons.  Tim Schowalter has presided over the Rotary Club
of Granby in challenging yet fulfilling times. He has exemplified the
core values of Rotary International while keeping our Club local and
important.  Thanks, Tim, for your hard work.  Jan Knisley has a strong
example to follow.</p>

