<h3>DollyWood Project</h3>
<p>
DollyWood project continues to gain in popularity, offering continued
positive exposure for our Club. The DollyWood Imagination Library
preschool reading project continues to see excellent growth in our
region, says Granby Rotarian Jack Bakken. As of Jan. 21 a total of 53
preschool-aged children had signed up! That exceeds our expectations.
This project, sponsored by the Rotary Club of Granby, Grand Beginnings
and the Grand County Library District, makes free books available to
preschool-aged children. They must commit to read the books or have
the books read to them by a parent or guardian.  The inclusion of the
zip codes in Hot Sulphur Springs and Tabernash for our coverage area
in the program seems to have helped the popularity of the program.

