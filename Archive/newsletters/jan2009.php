<h3>Granby Rotary Club Social Feb. 20</h3>
<p>
The Granby Rotary Club is hosting a club social
event Friday night, Feb. 20 at Maverick's in Granby.  Maverick's is
owned and operated by our own Rotary Club member Sean Richardson.
Cocktails and appetizers start at 6:30 p.m. in the lounge followed by
dinner in the large dining room at 7:30. Beverages include cash bar,
water, iced tea and coffee. Appetizers include bacon-wrapped shrimp,
mini-quiche and a vegetable platter.  Dinner includes prime rib
carving station and crusted sea bass with salad, dinner rolls, mashed
potatoes and gravy and a vegetable medley.  Dessert includes chocolate
fudge cake or caramel flan. Cost is $30 per person, which includes tax
and tip.  The cut-off for reservations is Feb. 11.
</p>
