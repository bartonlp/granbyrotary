<h3>Wedndsday April 1<sup>st</sup> Meeting Program by Mark Krieg</h3>

<p>Mark Krieg's Program to cover the topic of Grand County's
Headwaters Trails Alliance and recent improvements and enhancements,
and this is no &quot;April Fools&quot; joke. Mark Krieg, past
president of the Rotary Club of Granby and our Rotary District's
Assistant Governor, has the program April 1. His topic will be about
the Headwaters Trails Alliance.</p>

<p>Lucinda Elicker, with the Headwaters Trails Alliance of Grand
County, will be on hand to inform the Club's members about the latest
developments, improvements and enhancements that have taken place
because of work by this important agency. These improvements include a
new trail at Granby Ranch, improved trails near Tabernash and Fraser
and real work to link Fraser and Granby by way of an improved,
non-motorized trail.</p>

