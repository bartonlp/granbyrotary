<h3>Health Screenings At No Charge Or Low-Cost Offered At 9Health
   Fairs</h3>
 
<p>Free and low-cost health screenings will be offered at the 9Health
Fairs taking place in Kremmling, Granby and Fraser in April.  The
series of 9Health Fairs starts in at Kremmling at West Grand High
School April 4 (8 am. to noon), followed April 18 at Granby Elementary
School (7 to 11 a.m.) and April 25 at the Fraser Valley Elementary
School (7:30 to 11:45 a.m.).</p>

<p>The health care screenings and tests provided at the 9Health Fairs
are priced far below the regular costs for these tests, which include
a comprehensive blood chemistry analysis for $30, blood count test for
$15, prostate specific antigen screening for $25 and a colon
cancer-screening test for $20. Free screenings include height, weight
and body mass analysis, among many others.</p>

<p>New this year, health fair participants can pre-register on-line at
the Granby and Fraser sites by going to <a
href="http://9healthfair.org"> http://9healthfair.org</a> and going to
the on-line links:
<ul>
   <li><a
   href='https://service.rmdnetworks.com/9Health/9Health/info.aspx?siteid=159'>Granby
      Pre-registration</a></li>
   <li><a
   href='https://service.rmdnetworks.com/9Health/9Health/info.aspx?siteid=154'>Fraser
      Pre-registration</a></li>
</ul>

While day-of-event registration is
still offered at the fairs, the on-line registration does offer some
benefits. It allows for pre-payment, streamlined registration on the
day of the event, quicker access on-line to test results and longterm
availability and comparisons of test results over time.</p>

<p>Kremmling also offers on-site registration from 7 a.m. to 5 p.m.
Wednesday, April 1 at the Kremmling Memorial Hospital Conference
room.</p>

<p>Aside from the blood tests, screenings and other free screenings,
the 9Health Fairs feature educational exhibits manned by
representatives from Grand County Emergency Medical Services, the
American Cancer Society, The Grand County Rural Health Network and
others.</p>

<p>Because the 9Health Fairs are so popular, volunteers are always
needed and appreciated. People with or without a healthcare background
are encouraged to help out. Go to the <a href='/news.php#9HealthFair'>
Rotary Club of Granby website</a> to sign up. Or, go to the 9Health
Fair website above and look for the Granby site to sign up for an
opportunity to volunteer at the 9Health Fair in your community or
contact the sponsoring organization for each community.</p>

<p>Aside from the screenings and tests mentioned above, the 9Health
Fairs also offer: Vision tests, blood pressure checks, one-on-one
visits with a health professional, an &quot;ask-a-pharmacist&quot; site,
body-fat skin fold analysis, body in balance tests, eye health
screening, breast screening, finger stick glucose screening, foot
screening, hearing screening, a heart health corner, a lung function
screening, mental health screening and many other tests or
screenings.</p>

<p>Participants are reminded that for many of the blood tests a period
of <b>fasting</b> is required ahead of time, so people should plan on
fasting for at least <span style='color: red'>12 hours</span> prior to
arriving at the health fair.  Participants are also reminded that
these health fairs are popular, which means they should plan on
arriving early for the most efficient treatment.</p>

<p>To find more information on each fair, visit <a
href='http://www.gcruralhealth.com'>www.gcruralhealth.com</a> or go to
<a href='http://www.9healthfair.org'>www.9healthfair.org</a>. Or, call
the Grand County Rural Health Network at 887-3064.</p>

