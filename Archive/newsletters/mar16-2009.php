<h3>Health Fair Volunteers Being Sought</h3>

<p> Sign up now for this important event on web site: <a
href='http://www.9healthfair.org/volunteer/volunteersignup.aspx'>Volunteer</a>.

The Granby Rotary Club is the major sponsor of the 9Health Fair, set
to take place April 18 at Granby Elementary School.  Volunteers are
very important for this event. So, sign up today for a variety of jobs
