<h3>Winter Park Rotary Invites All To Annual Awards Meeting</h3> 

<p>The Winter Park Rotary Cub has announced its annual awards dinner,
which is set to take place Saturday, April 4 at Beavers in Winter
Park. All Rotarians are invited to this festive evening.</p>

<p>This year's Mike Muftic "Service Above Self" award winner is Kirk
Klancke. The Rotarian of the Year award is going to Will Arduino.</p>

<p>Cocktails start at 6 p.m. with some entertainment. Dinner is at
7:30 p.m. with the awards presentation at 8:30 p.m. Tickets can be
purchased at the door for $30, but reservations are encouraged.
Contact Steve Radcliffe at ridgecliffe@msn.com or phone him at
726-4454.</p>

